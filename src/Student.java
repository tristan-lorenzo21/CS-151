/**
 *  A class for declaring a student's name
 */

public class Student {
    /**
     * Instance variable for the student's name
     */
    String name;

    /**
     * Constructs a Student object that initializes the name of the student
     *
     * @param name the name of the student that is submitting this assignment
     */
    Student(String name) {
        this.name = name;
    }
}
